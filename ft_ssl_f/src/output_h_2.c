#include "../includes/ft_ssl.h"

void	print_hash_hex(unsigned char c)
{
	int		num;
	char	print;

	num = (int)c;
	print = (num / 16) % 16 + ((num / 16 % 16 > 9) ? (-10 + 'a') : '0');
	write(1, &print, 1);
	print = num % 16 + ((num % 16 > 9) ? (-10 + 'a') : '0');
	write(1, &print, 1);
}
