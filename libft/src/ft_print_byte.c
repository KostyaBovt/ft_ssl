#include "../includes/libft.h"

void	ft_print_byte(unsigned char c)
{
	int				i;
	unsigned char	shift;

	i = 7;
	shift = 1;
	while (i >= 0)
	{
		(c & (shift << i)) ? write(1, "1", 1) : write(1, "0", 1);
		i--;
	}
}
